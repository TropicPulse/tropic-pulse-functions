function createMailer() {
  const SMTP_HOST = process.env.SMTP_HOST || "smtp.gmail.com";
  const SMTP_USER = process.env.SMTP_USER || process.env.EMAIL_USER;
  const SMTP_PASS = process.env.EMAIL_PASSWORD || process.env.SMTP_PASS;
  const ALERT_EMAIL_FROM =
    process.env.ALERT_EMAIL_FROM || `"Pulse OS" <no-reply@pulse.local>`;
  const ALERT_EMAIL_TO =
    process.env.ALERT_EMAIL_TO || "FordFamilyDelivery@gmail.com";

  if (!SMTP_USER || !SMTP_PASS) {
    console.warn("[timer.js] SMTP credentials missing – email disabled.");
    return null;
  }

  const transporter = nodemailer.createTransport({
    host: SMTP_HOST,
    port: 465,
    secure: true,
    auth: { user: SMTP_USER, pass: SMTP_PASS }
  });

  return { transporter, from: ALERT_EMAIL_FROM, to: ALERT_EMAIL_TO };
}

