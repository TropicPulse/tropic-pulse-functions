function deriveFilePathFromLog(log) {
  if (log.filePath) return log.filePath;

  const pagePath = log.pagePath || "/";
  const fileName = log.fileName || "index.js";

  const cleanPage = pagePath.replace(/^\/+/, "");
  return path.join(DEFAULT_PAGES_ROOT, cleanPage || "", fileName);
}

function groupLogs(logDocs) {
  const filesMap = new Map();

  for (const { id, data } of logDocs) {
    const log = data;
    const filePath = deriveFilePathFromLog(log);
    const pagePath = log.pagePath || "UNKNOWN_PAGE";
    const fnName = log.functionName || "unnamedFunction";

    if (!filesMap.has(filePath)) {
      filesMap.set(filePath, {
        pagePath,
        logs: [],
        functions: new Map()
      });
    }

    const fileEntry = filesMap.get(filePath);
    fileEntry.logs.push({ id, log });

    if (!fileEntry.functions.has(fnName)) {
      fileEntry.functions.set(fnName, {
        hasMissingFunction: false,
        fields: new Set()
      });
    }

    const fnEntry = fileEntry.functions.get(fnName);

    if (log.type === "missing_function") {
      fnEntry.hasMissingFunction = true;
    }

    if (log.type === "missing_field" && log.fieldName) {
      fnEntry.fields.add(log.fieldName);
    }
  }

  return filesMap;
}

function buildJSBlock(pagePath, filePath, functionsMap) {
  const now = new Date().toISOString();
  let block = "";

  block += "\n\n";
  block += "// #%#%#%#%#%#%#%#%#%#%#%#%#\n";
  block += "// # AUTO-GENERATED BY PULSE OS\n";
  block += `// # DATE: ${now}\n`;
  block += `// # PAGE: ${pagePath}\n`;
  block += `// # FILE: ${filePath}\n`;
  block += "// #%#%#%#%#%#%#%#%#%#%#%#%#\n\n";

  for (const [fnName, info] of functionsMap.entries()) {
    const fields = Array.from(info.fields);

    if (info.hasMissingFunction) {
      block += `// Auto-generated function stub for: ${fnName}\n`;
      block += `function ${fnName}() {\n`;
      if (fields.length > 0) {
        block += "  return {\n";
        for (const field of fields) {
          block += `    ${field}: null, // TODO auto-generated field\n`;
        }
        block += "  };\n";
      } else {
        block += "  return {};\n";
      }
      block += "}\n\n";
    } else if (fields.length > 0) {
      const helperName = `ensure_${fnName}_fields`;
      block += `// Auto-generated helper for: ${fnName}\n`;
      block += `function ${helperName}() {\n`;
      block += "  return {\n";
      for (const field of fields) {
        block += `    ${field}: null, // TODO auto-generated field\n`;
      }
      block += "  };\n";
      block += "}\n\n";
    }
  }

  block += "// #^#^#^#^#^#^#^#^#^#^#^#^#\n";
  block += "// # END AUTO-GENERATED BLOCK\n";
  block += "// #^#^#^#^#^#^#^#^#^#^#^#^#\n";

  return block;
}

function insertIntoHTML(original, block) {
  const lower = original.toLowerCase();

  const lastScriptIndex = lower.lastIndexOf("</script>");
  const lastBodyIndex = lower.lastIndexOf("</body>");

  if (lastScriptIndex !== -1 && lastScriptIndex > lastBodyIndex - 200) {
    return (
      original.slice(0, lastScriptIndex) +
      block +
      original.slice(lastScriptIndex)
    );
  }

  if (lastBodyIndex !== -1) {
    return (
      original.slice(0, lastBodyIndex) +
      `<script>${block}</script>\n` +
      original.slice(lastBodyIndex)
    );
  }

  return original + `<script>${block}</script>`;
}

function buildNewFilePath(originalPath) {
  const ext = path.extname(originalPath);
  const base = originalPath.slice(0, -ext.length);
  return `${base}-NEW${ext}`;
}

