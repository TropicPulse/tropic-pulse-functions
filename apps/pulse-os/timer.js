async function sendEmail(mailer, files) {
  if (!mailer || !files.length) return;

  const subject = "Pulse OS – Auto-Generated Files Ready";
  const body = [
    "Pulse OS has generated new files:",
    "",
    ...files.map(f => ` - ${f}`),
    "",
    "Review the bottom of each file for the AUTO-GENERATED block."
  ].join("\n");

  await mailer.transporter.sendMail({
    from: mailer.from,
    to: mailer.to,
    subject,
    text: body
  });
}

// -----------------------------
// MIDNIGHT TIMER
// -----------------------------

export default function startPulseTimer() {
  const db = getFirestore();
  const mailer = createMailer();

  let lastRun = null;

  setInterval(async () => {
    const now = new Date();
    const today = now.toISOString().slice(0, 10);

    const inWindow =
      now.getHours() === 0 && now.getMinutes() < MIDNIGHT_WINDOW_MINUTES;

    if (!inWindow) return;
    if (lastRun === today) return;

    lastRun = today;

    const { generatedFiles } = await processLogs(db);
    await sendEmail(mailer, generatedFiles);
  }, 60 * 1000);
}