// FIRESTORE + FILE WRITING
// -----------------------------

async function fetchUnprocessedLogs(db) {
  const snap = await db
    .collection(FUNCTION_LOGS_COLLECTION)
    .where("processed", "==", false)
    .get();

  const docs = [];
  snap.forEach(doc => docs.push({ id: doc.id, data: doc.data() }));
  return docs;
}

async function processLogs(db) {
  const logs = await fetchUnprocessedLogs(db);
  if (!logs.length) return { generatedFiles: [] };

  const filesMap = groupLogs(logs);
  const generatedFiles = [];
  const batch = db.batch();
  const nowTs = Timestamp.now();

  for (const [filePath, entry] of filesMap.entries()) {
    try {
      const original = fs.readFileSync(filePath, "utf8");
      const ext = path.extname(filePath).toLowerCase();

      const jsBlock = buildJSBlock(
        entry.pagePath,
        filePath,
        entry.functions
      );

      let newContent =
        ext === ".html"
          ? insertIntoHTML(original, jsBlock)
          : original + jsBlock;

      const newFilePath = buildNewFilePath(filePath);

      fs.writeFileSync(newFilePath, newContent, "utf8");
      generatedFiles.push(newFilePath);

      for (const { id } of entry.logs) {
        const ref = db.collection(FUNCTION_LOGS_COLLECTION).doc(id);
        batch.update(ref, { processed: true, processedAt: nowTs });
      }
    } catch (err) {
      console.error("[timer.js] Failed to process:", filePath, err);
    }
  }

  await batch.commit();
  return { generatedFiles };
}

